package basic_program_ab42;

public class For_loop_programs {

	public static void main(String[] args) {
	for(int i=0;i<4;i++)
	{
     System.out.println("India");
	}
    for(int i=0;i<=4;i++)
    {
     System.out.println("Anitha");	
    }
    for(int i=0;i<=10;i++)  
    {	
    	System.out.println("Lohith");
    }
    for(int j=10;j>=0;j--)
    {
    	System.out.println(j);
    }
    
}}